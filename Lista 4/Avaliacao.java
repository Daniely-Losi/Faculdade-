package Exer5;

public class Avaliacao { 
	
	private int qtdPagantes;
	private String opinao;

	 
	public Avaliacao(int qtdPagantes, String opinao) {
		this.qtdPagantes = qtdPagantes;
		this.opinao = opinao;
	}


	public int getQtdPagantes() {
		return qtdPagantes;
	}


	public void setQtdPagantes(int qtdPagantes) {
		this.qtdPagantes = qtdPagantes;
	}


	public String getOpinao() {
		return opinao;
	}


	public void setOpinao(String opinao) {
		this.opinao = opinao;
	}

	
	
	
	

}